package day3;

public class ForTest5 {
	public static void main(String[] args) {

		for (int n = 10; n >= 1; n--)
			System.out.println(n+":"+n*n);		
		System.out.println("-------------------");
		
		for (int n = 10; n <=20; n = n+3)
			System.out.println(n+":"+n*n);		
		System.out.println("-------------------");
	}
	

}
